CREATE OR REPLACE TRIGGER Update_Salary
BEFORE UPDATE 
ON GRADE_PAY 
FOR EACH ROW
DECLARE
SAL_DIFF NUMBER;
BEGIN
SAL_DIFF:=(:NEW.BASIC_PAY+:NEW.HRA+:NEW.DA)-(:OLD.BASIC_PAY+:OLD.HRA+:OLD.DA);
DBMS_OUTPUT.PUT_LINE('OLD SALARY: '||(:OLD.BASIC_PAY+:OLD.HRA+:OLD.DA));
DBMS_OUTPUT.PUT_LINE('NEW SALARY: '||(:NEW.BASIC_PAY+:NEW.HRA+:NEW.DA));
DBMS_OUTPUT.PUT_LINE('INCREMENT IN SALARY:  '||SAL_DIFF);
END;
/

